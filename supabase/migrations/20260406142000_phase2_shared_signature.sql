create or replace function public.public_patient_sign_request(
  p_token text,
  p_patient_signature text,
  p_user_agent text default null,
  p_timezone text default null,
  p_locale text default null,
  p_route_or_origin text default null
)
returns boolean
language plpgsql
security definer
set search_path = public
as $$
declare
  v_request public.sign_requests%rowtype;
begin
  select *
    into v_request
  from public.sign_requests sr
  where sr.sign_token = p_token
    and access_code_validated_at is not null
    and consent_accepted_at is not null
    and sr.status in ('awaiting_patient', 'opened', 'completed')
  limit 1;

  if not found then
    return false;
  end if;

  if v_request.patient_signed_at is null then
    update public.sign_requests
    set patient_signature = p_patient_signature,
        patient_signed_at = now(),
        completed_at = now(),
        status = 'completed',
        updated_at = now()
    where public.sign_requests.id = v_request.id;

    insert into public.request_events (
      request_id,
      event_type,
      actor_type,
      user_agent,
      timezone,
      locale,
      route_or_origin
    )
    values (
      v_request.id,
      'patient_signed',
      'patient',
      p_user_agent,
      p_timezone,
      p_locale,
      p_route_or_origin
    );

    insert into public.request_events (
      request_id,
      event_type,
      actor_type,
      user_agent,
      timezone,
      locale,
      route_or_origin
    )
    values (
      v_request.id,
      'request_completed',
      'patient',
      p_user_agent,
      p_timezone,
      p_locale,
      p_route_or_origin
    );
  end if;

  return true;
end;
$$;

